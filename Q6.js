var idade= parseInt(prompt("Digite sua idade"));
if(5<=idade & idade<=7){
    console.log("Infantil A")
   
}
if(8<=idade & idade<=10){
    console.log("Infantil B")
    
}
if(11<=idade & idade<=13){
    console.log("Juvenil A")
    
}
if(14<=idade & idade<=17){
    console.log("Juvenil B")

}
if(idade>=18){
    console.log("Senior")
}
